package com.eedu.diagnosis.manager.model.request.ClassTest.machine;

public class UserAnswer {
	private Integer id;
	private String testCode;
	private String exercisesCode;
	private String keyPadCode;
	private String answer;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getTestCode() {
		return testCode;
	}

	public void setTestCode(String testCode) {
		this.testCode = testCode;
	}

	public String getExercisesCode() {
		return exercisesCode;
	}

	public void setExercisesCode(String exercisesCode) {
		this.exercisesCode = exercisesCode;
	}

	public String getKeyPadCode() {
		return keyPadCode;
	}

	public void setKeyPadCode(String keyPadCode) {
		this.keyPadCode = keyPadCode;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

}
